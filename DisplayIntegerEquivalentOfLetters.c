#include<stdio.h>
void main(){
	char ch;
	printf("Ascii Value of Lowercase Letters:\n");   
	for(ch='a';ch<='z';ch++){
	printf("Ascii value of %c is %d\n",ch,ch);
}
printf("Ascii value of Upper case letters:\n");
for(ch='A';ch<='Z';ch++){
	printf("Ascii value of %c is %d\n",ch,ch);
}
	
	
}
